from math import sqrt, sin, cos, tan


def soma(valor1, valor2):
  return valor1 + valor2

def subtracao(valor1, valor2):
  return valor1 - valor2

def multiplicacao(valor1, valor2):
  return valor1 * valor2

def divisao(valor1, valor2):
  return valor1 / valor2

def exponenciacao(valor, expoente):
  return valor**expoente

def raiz_quadrada(valor):
  return sqrt(valor)

def raiz_nesima(valor, indice):
  return valor ** (1/indice)

def seno(valor):
  return sin(valor)

def cosseno(valor):
  return cos(valor)

def tangente(valor):
  return tan(valor)

def mudar_sinal(valor):
  return -(valor)


